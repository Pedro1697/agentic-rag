from langchain_core.prompts import ChatPromptTemplate
from pydantic import BaseModel, Field
from langchain_google_genai import ChatGoogleGenerativeAI
from dotenv import load_dotenv
load_dotenv()


llm = ChatGoogleGenerativeAI(model="gemini-2.5-flash")

class GradeDocuments(BaseModel):
    """Binary score for revelance check on retrieved documents."""

    binary_score: str = Field(
        description="docuemnts are revelant to the quetion, 'yes'or 'no'"
    )

structured_llm_grader = llm.with_structured_output(GradeDocuments)

system = """You are a grader assessing revelance of a retrieved document to a user question. \n
        If the document contains. keywords(s) or semantic meaning related to the question, grade it as revelevant. \n
        Give a binary score 'yes'or 'no'score to indicate whether the document is relevant to the question."""


grade_prompt = ChatPromptTemplate.from_messages(
    [
        ("system",system),
        ("human","Retrieved document: \n\n {document}\n\n User question:{question}")
    ]
)

retrieval_grader = grade_prompt | structured_llm_grader